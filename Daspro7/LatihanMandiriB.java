package Daspro7;

public class LatihanMandiriB {
    public static void main(String[] args) {
        int hasil = 0;

        for (int i = 25; i >= 1; i--) {
            hasil += i;
        }

        System.out.println("Hasil penjumlahan deret bilangan dari 25 sampai dengan 1 adalah: " + hasil);
    }
}